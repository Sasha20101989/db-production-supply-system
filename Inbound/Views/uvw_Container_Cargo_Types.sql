CREATE VIEW Inbound.uvw_Container_Cargo_Types
AS
SELECT        Inbound.tbd_Parts_In_Container.Container_In_Lot_Id, CASE WHEN MAX(Part_Type_Id) <> MIN(Part_Type_Id) THEN 'MIX' WHEN MAX(Part_Type_Id) = 1 THEN 'BODY' ELSE 'PARTS' END AS Container_Cargo_Type
FROM            Inbound.tbd_Parts_In_Container INNER JOIN
                         Customs.tbd_Customs_Parts ON Inbound.tbd_Parts_In_Container.Part_Number_Id = Customs.tbd_Customs_Parts.Part_Number_Id
GROUP BY Inbound.tbd_Parts_In_Container.Container_In_Lot_Id
GO
